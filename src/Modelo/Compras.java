package Modelo;

/**
 *
 * @author usuario
 */
public class Compras {
    private String identificacion;
    private String insumo;
    private String insumoTipo;
    private String precioInsumo;
    private String cantidad;
    private String precioCantidad;

    /**
     * @return the identificacion
     */
    public String getIdentificacion() {
        return identificacion;
    }

    /**
     * @param identificacion the identificacion to set
     */
    public void setIdentificacion(String identificacion) {
        this.identificacion = identificacion;
    }

    /**
     * @return the insumo
     */
    public String getInsumo() {
        return insumo;
    }

    /**
     * @param insumo the insumo to set
     */
    public void setInsumo(String insumo) {
        this.insumo = insumo;
    }

    /**
     * @return the insumoTipo
     */
    public String getInsumoTipo() {
        return insumoTipo;
    }

    /**
     * @param insumoTipo the insumoTipo to set
     */
    public void setInsumoTipo(String insumoTipo) {
        this.insumoTipo = insumoTipo;
    }

    /**
     * @return the precioInsumo
     */
    public String getPrecioInsumo() {
        return precioInsumo;
    }

    /**
     * @param precioInsumo the precioInsumo to set
     */
    public void setPrecioInsumo(String precioInsumo) {
        this.precioInsumo = precioInsumo;
    }

    /**
     * @return the cantidad
     */
    public String getCantidad() {
        return cantidad;
    }

    /**
     * @param cantidad the cantidad to set
     */
    public void setCantidad(String cantidad) {
        this.cantidad = cantidad;
    }

    /**
     * @return the precioCantidad
     */
    public String getPrecioCantidad() {
        return precioCantidad;
    }

    /**
     * @param precioCantidad the precioCantidad to set
     */
    public void setPrecioCantidad(String precioCantidad) {
        this.precioCantidad = precioCantidad;
    }
    
  
    
}
