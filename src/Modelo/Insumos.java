
package Modelo;


public class Insumos {
    
    private String categoria;
    private String nombreInsumo;
    private String codigo;
    private String cantidad;
    private String precioInsumo;

    /**
     * @return the categoria
     */
    public String getCategoria() {
        return categoria;
    }

    /**
     * @param categoria the categoria to set
     */
    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    /**
     * @return the nombreInsumo
     */
    public String getNombreInsumo() {
        return nombreInsumo;
    }

    /**
     * @param nombreInsumo the nombreInsumo to set
     */
    public void setNombreInsumo(String nombreInsumo) {
        this.nombreInsumo = nombreInsumo;
    }

    /**
     * @return the codigo
     */
    public String getCodigo() {
        return codigo;
    }

    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    /**
     * @return the cantidad
     */
    public String getCantidad() {
        return cantidad;
    }

    /**
     * @param cantidad the cantidad to set
     */
    public void setCantidad(String cantidad) {
        this.cantidad = cantidad;
    }

    /**
     * @return the precioInsumo
     */
    public String getPrecioInsumo() {
        return precioInsumo;
    }

    /**
     * @param precioInsumo the precioInsumo to set
     */
    public void setPrecioInsumo(String precioInsumo) {
        this.precioInsumo = precioInsumo;
    }


    
    
    
    
    
    
}
